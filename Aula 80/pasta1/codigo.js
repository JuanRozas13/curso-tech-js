function Troca_claro ()
{
    let fundo = document.getElementById("fundo");
    fundo.style.backgroundColor = "white";
    fundo.style.color = "black";
}

function Troca_escuro ()
{
    let fundo = document.getElementById("fundo");
    fundo.style.backgroundColor = "black";
    fundo.style.color ="white";
}

